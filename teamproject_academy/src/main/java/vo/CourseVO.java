package vo;

public class CourseVO {
	private int lno;
	private int sno;
	private int cno;
	private int cyn;
	private String cgrade;
	private int cdelyn;
	
	
	public int getLno() {
		return lno;
	}
	public void setLno(int lno) {
		this.lno = lno;
	}
	public int getSno() {
		return sno;
	}
	public void setSno(int sno) {
		this.sno = sno;
	}
	public int getCdelyn() {
		return cdelyn;
	}
	public void setCdelyn(int cdelyn) {
		this.cdelyn = cdelyn;
	}
	public int getCno() {
		return cno;
	}
	public void setCno(int cno) {
		this.cno = cno;
	}
	public int getCyn() {
		return cyn;
	}
	public void setCyn(int cyn) {
		this.cyn = cyn;
	}
	public String getCgrade() {
		return cgrade;
	}
	public void setCgrade(String cgrade) {
		this.cgrade = cgrade;
	}
	
}
